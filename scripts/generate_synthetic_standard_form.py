def build_document(playbook_id: str = playbook_registry.DEFAULT_PLAYBOOK_ID):
    try:
        from docx import Document  # type: ignore
    except ImportError:
        print(
            "ERROR: python-docx is required to generate the synthetic "
            "standard form.\n  pip install python-docx",
            file=sys.stderr,
        )
        sys.exit(1)

    section_config = bam.load_section_config(playbook_id)
    config = section_config["sections"]
    absent_from_form_anchors = section_config["absent_from_form_anchors"]
    sub_clause_splits = section_config["sub_clause_splits"]

    doc = Document()
    # Plain (non-Heading-style) front-matter paragraphs -- deliberately NOT
    # `add_heading()` so they are inert with respect to the loader's
    # heading-matching logic (they match no anchor and are simply discarded
    # as unattached pre-first-section text, with no WARNING noise from a
    # heading-style paragraph matching nothing).
    doc.add_paragraph("EIAA Standard Form (SYNTHETIC placeholder — issue #200)")
    doc.add_paragraph(
        "This document is a SYNTHETIC engineering placeholder, not the real "
        "EIAA standard-form text. See standard-forms/README.md."
    )

    emitted_parents = set()

    for anchor, heading, sub_split, parent in config:
        if anchor in absent_from_form_anchors:
            # Deliberately absent from the real form -- omit entirely (see
            # module docstring).
            continue

        if sub_split:
            if parent in emitted_parents:
                continue
            group = sub_clause_splits[parent]
            doc.add_heading(group["source_heading"], level=2)
            for split in group["splits"]:
                body = SEC_10_SPLIT_BODY.get(split["anchor"])
                if body is None:
                    raise KeyError(
                        f"No SEC_10_SPLIT_BODY placeholder text for anchor "
                        f"{split['anchor']!r}"
                    )
                doc.add_paragraph(body)
            emitted_parents.add(parent)
            continue

        doc.add_heading(heading, level=2)
        body = BODY_TEXT.get(anchor)
        if body is None:
            raise KeyError(f"No BODY_TEXT placeholder text for anchor {anchor!r}")
        doc.add_paragraph(body)

    return doc


def main() -> int:
    parser = argparse.ArgumentParser(
        description="Generate the SYNTHETIC placeholder standard-form .docx (issue #200)."
    )
    parser.add_argument(
        "--playbook-id",
        type=str,
        default=playbook_registry.DEFAULT_PLAYBOOK_ID,
    )
    parser.add_argument("--output", type=Path, default=DEFAULT_OUTPUT)
    args = parser.parse_args()

    doc = build_document(args.playbook_id)
    args.output.parent.mkdir(parents=True, exist_ok=True)
    doc.save(str(args.output))
    print(f"Wrote SYNTHETIC placeholder standard form to: {args.output}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
