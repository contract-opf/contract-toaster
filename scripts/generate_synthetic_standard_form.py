#!/usr/bin/env python3
"""
Generate the SYNTHETIC placeholder standard-form .docx (issue #200).

## Problem this solves

There is no canonical standard-form .docx in the repo yet (see
standard-forms/README.md): the real EIAA form text is a GC deliverable, not
something engineering can fabricate. Without ANY real .docx, real-docx mode
(scripts/build_anchor_map.py --docx, scripts/diff_standard_form.py --docx)
is never exercised, and the §10 sub-clause-split loader logic added in issue
#200 has nothing to run against.

This script generates a clearly-labeled SYNTHETIC placeholder .docx --
standard-forms/eiaa-v1.0.0.SYNTHETIC.docx -- that faithfully expresses the
required *structure* of the real form (every registered section anchor,
including the §10 sub-clause split under a single "Miscellaneous" heading)
with short, generic, de-branded placeholder prose. It is NOT the real EIAA
standard-form text and must never be treated as one -- see
standard-forms/README.md.

When the real .docx is available, it replaces this file directly (same
filename convention swapped for a non-SYNTHETIC one, or this file is
deleted and the registry/CLI --docx flag points at the real file instead);
the loader/anchor-map/diff code added in issue #200 already works against
real OOXML and requires no code change.

## Heading text convention

Headings are typed WITHOUT a manually-typed leading section number (e.g.
"Admitting Students", not "1.2 Admitting Students") -- matching how a real
Word document using outline/multilevel-list auto-numbering linked to the
Heading styles renders: the visible "1.2" is generated by the numbering
definition, not stored in the paragraph's run text, so python-docx's
`paragraph.text` does not include it. A manually typed number in the
heading text (rather than real Word auto-numbering) would NOT match the
anchor map's plain heading strings -- see issue #200's "Required
verification" point 4. This fixture intentionally avoids that failure mode
by construction.

## §10 sub-clause split

`sec-2.2.1` ("Without Cause") is `absent_from_form` per
playbooks/eiaa-v1.0.0.sections.json and is deliberately OMITTED from this
document -- Exos's real form does not grant this right, and the anchor
exists in the map only so the coverage gate can flag a counterparty who
tries to introduce it.

Usage:
    python3 scripts/generate_synthetic_standard_form.py
    python3 scripts/generate_synthetic_standard_form.py --output <path>
"""

