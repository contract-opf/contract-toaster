#!/usr/bin/env python3
"""
Slice test (TDD) for issue #204: "The eval harness measures regex
self-consistency, not review quality -- no model call, no documents, and
redline correctness is checked nowhere".

## Root problem this proves fixed

Before this slice, scripts/eval_harness.py never invoked a model
(FakeBedrockClient or otherwise) and never ran a real `.docx` through the
extract -> normalize -> diff -> detector chain -- every gold fixture in
tests/gold-fixtures/ carried `redline_checks: []` and no `input_docx`, so
of docs/evaluation.md's four per-case checks only a snippet-level fire/
no-fire subset ever ran, and 15 of the pre-#183/#204 fixtures were
mechanically generated by building the planted hunk FROM the rule's own
first trigger term -- a tautology (the harness verified a regex matches a
string constructed to match it).

This test FAILS on a tree without `scripts/eval_harness.py`'s
`score_document_level_case` / `check_redline_checks` /
`score_smoke_tier_case` functions (AttributeError on the module-level
lookups below) and PASSES once all five items in the issue's Required
verification are satisfied.

## What this test asserts (mirrors the issue's Required verification)

  1. Document-level gold case: `tests/fixtures/gold_docx_204/
     reject-no-exclusivity-doc-level.{docx,json}` is a real, committed,
     clearly-labeled SYNTHETIC .docx fixture (`input_docx` populated,
     content-hashed) run through the ACTUAL extract -> normalize -> diff ->
     detector chain (scripts/extraction_normalization_stage.py ->
     scripts/diff_standard_form.py -> scripts/detector_common.py), not a
     pre-isolated text snippet -- and it correctly scores decision
     accuracy / missed-issue / false-positive.
  2. `redline_checks[]` on that same case pins `(anchor, source_text_hash)`
     for the reject case, and is asserted against the ACTUAL redline/patch
     output (scripts/redline_patch.py's server-side join + apply_patch),
     not merely present-but-ignored.
  3. A smoke-tier "model" pass, driven by the deterministic
     `FakeBedrockClient` + RECORDED response fixtures from #81
     (tests/fixtures/model_responses/*.json -- no live Bedrock, no
     network), measures decision accuracy + missed-issue rate against a
     known answer through the real `primary_review_pass.run_primary_pass`
     assembly/validation code.
  4. The generator tautology is broken: at least one committed gold
     fixture carries independent, hand-authored signal the mechanical
     generator (scripts/generate_gold_fixtures.py) cannot produce -- no
     `generated_by` field, `case_type` in {"near_miss", "injection"}.
  5. docs/evaluation.md's "Recorded baseline (v1)" table is relabeled a
     PROJECTION until a live stochastic gate run replaces it.

Run with: python3 tests/test_eval_harness_quality_204.py
Exit codes: 0 = pass, 1 = fail
"""

from __future__ import annotations

import hashlib
import json
import sys
from pathlib import Path
from typing import Any

REPO_ROOT = Path(__file__).resolve().parent.parent
SCRIPTS_DIR = REPO_ROOT / "scripts"
BACKEND_SRC_DIR = REPO_ROOT / "backend" / "src"
GOLD_FIXTURES_DIR = REPO_ROOT / "tests" / "gold-fixtures"
GOLD_DOCX_FIXTURES_DIR = REPO_ROOT / "tests" / "fixtures" / "gold_docx_204"
MODEL_RESPONSES_DIR = REPO_ROOT / "tests" / "fixtures" / "model_responses"
PLAYBOOK_PATH = REPO_ROOT / "playbooks" / "eiaa-v1.0.0.json"
EVALUATION_DOC_PATH = REPO_ROOT / "docs" / "evaluation.md"

for _dir in (SCRIPTS_DIR, BACKEND_SRC_DIR):
    if str(_dir) not in sys.path:
        sys.path.insert(0, str(_dir))

import diff_standard_form  # noqa: E402
import eval_harness  # noqa: E402
import model_client  # noqa: E402

sys.path.insert(0, str(GOLD_DOCX_FIXTURES_DIR))
import _generate as gold_docx_204_generate  # noqa: E402


def _load_playbook() -> dict[str, Any]:
    with open(PLAYBOOK_PATH, "r", encoding="utf-8") as fh:
        return json.load(fh)


def _load_doc_level_case() -> tuple[dict[str, Any], bytes]:
    json_path = GOLD_DOCX_FIXTURES_DIR / "reject-no-exclusivity-doc-level.json"
    docx_path = GOLD_DOCX_FIXTURES_DIR / "reject-no-exclusivity-doc-level.docx"
    with open(json_path, "r", encoding="utf-8") as fh:
        case_raw = json.load(fh)
    docx_bytes = docx_path.read_bytes()
    return case_raw, docx_bytes


# ---------------------------------------------------------------------------
# 1. Document-level gold case: real extract -> normalize -> diff -> detector
#    chain over a committed, content-hashed, clearly-labeled SYNTHETIC
#    .docx fixture.
# ---------------------------------------------------------------------------


def test_document_level_fixture_is_real_docx_not_a_snippet(failures: list[str]) -> None:
    case_raw, docx_bytes = _load_doc_level_case()

    if not case_raw.get("input_docx"):
        failures.append("[1a] Document-level gold case is missing a populated 'input_docx' field.")
    if case_raw.get("synthetic") is not True:
        failures.append("[1b] Document-level gold case must be clearly labeled synthetic=true.")
    if case_raw.get("gc_signoff") != "pending":
        failures.append(
            "[1c] Document-level gold case must record gc_signoff='pending' -- not "
            "production-qualified until Legal/GC review, per the #183/#204 owner-approved "
            "synthetic scaffold convention."
        )

    # The fixture must be an actual OOXML .docx ZIP -- not a text snippet
    # standing in for one.
    if docx_bytes[:2] != b"PK":
        failures.append("[1d] input_docx fixture is not a valid ZIP/.docx (missing PK magic bytes).")

    expected_hash = "sha256:" + hashlib.sha256(docx_bytes).hexdigest()
    if case_raw.get("input_docx_sha256") != expected_hash:
        failures.append(
            f"[1e] input_docx is not content-hashed correctly: fixture records "
            f"{case_raw.get('input_docx_sha256')!r}, actual is {expected_hash!r}."
        )


def test_document_level_case_scores_via_real_pipeline(failures: list[str]) -> None:
    playbook = _load_playbook()
    standard = diff_standard_form.load_standard_form_paragraphs(docx_path=None, playbook_id="eiaa")
    case_raw, docx_bytes = _load_doc_level_case()

    result = eval_harness.score_document_level_case(case_raw, docx_bytes, playbook, standard)

    if not result.passed:
        failures.append(f"[1f] Document-level case did not pass: {result.reasons}")

    if "no-exclusivity" not in result.fired_rule_ids:
        failures.append(
            f"[1g] Expected the real diff+detector chain to fire 'no-exclusivity' on the "
            f"planted insertion; actual fires: {result.fired_rule_ids}"
        )

    target_hunk = next((h for h in result.hunks if h["anchor"] == "sec-10-non-exclusive"), None)
    if target_hunk is None:
        failures.append("[1h] No diff hunk was produced at anchor 'sec-10-non-exclusive'.")
    elif target_hunk["kind"] != "modified_new":
        failures.append(
            f"[1i] Expected the planted-insertion hunk to be kind='modified_new' "
            f"(real diff output), got {target_hunk['kind']!r}."
        )

    # An unrelated, untouched anchor from the SAME document must diff as
    # "unchanged" -- proving this is a real document-wide diff, not an
    # isolated single-hunk simulation.
    term_hunk = next((h for h in result.hunks if h["anchor"] == "sec-2.1"), None)
    if term_hunk is None or term_hunk["kind"] != "unchanged":
        failures.append(
            f"[1j] Expected anchor 'sec-2.1' (untouched by the plant) to diff as "
            f"'unchanged'; got {term_hunk!r}."
        )


def test_document_level_case_fails_red_on_a_clean_document(failures: list[str]) -> None:
    """False-positive control: running the SAME real pipeline over the
    verbatim (unmodified) standard form must yield zero fires / ACCEPT --
    proving the detector fire in the prior test is caused by the planted
    insertion, not a bug that fires unconditionally."""
    playbook = _load_playbook()
    standard = diff_standard_form.load_standard_form_paragraphs(docx_path=None, playbook_id="eiaa")

    # A verbatim draft: every standard-form anchor carried over unmodified
    # (reuses the same full-document builder the committed fixture's
    # generator uses -- see tests/fixtures/gold_docx_204/_generate.py).
    std_by_anchor = {p["anchor"]: p for p in standard}
    unplanted_text = std_by_anchor[gold_docx_204_generate.TARGET_ANCHOR]["text"]
    body_xml = gold_docx_204_generate.build_draft_body_xml(
        standard, gold_docx_204_generate.TARGET_ANCHOR, unplanted_text
    )
    clean_docx_bytes = gold_docx_204_generate._build_docx_bytes(body_xml)

    clean_case = {
        "case_id": "clean-control-doc-level",
        "expected_decision": "ACCEPT",
        "expected_issues": [],
        "must_not_flag": [],
        "fp_tolerance": 0,
    }
    result = eval_harness.score_document_level_case(clean_case, clean_docx_bytes, playbook, standard)
    if not result.passed:
        failures.append(
            f"[1k] The verbatim-standard-form control document must ACCEPT with zero fires; "
            f"got passed={result.passed}, fired_rule_ids={result.fired_rule_ids}, "
            f"reasons={result.reasons}"
        )


# ---------------------------------------------------------------------------
# 2. redline_checks[] populated and asserted against real redline/patch
#    output.
# ---------------------------------------------------------------------------


def test_redline_checks_populated_and_verified_against_patch_output(failures: list[str]) -> None:
    case_raw, docx_bytes = _load_doc_level_case()
    playbook = _load_playbook()
    standard = diff_standard_form.load_standard_form_paragraphs(docx_path=None, playbook_id="eiaa")

    redline_checks = case_raw.get("redline_checks", [])
    if not redline_checks:
        failures.append(
            "[2a] Document-level reject case must carry a populated redline_checks[] -- "
            "docs/evaluation.md 'What a gold case contains' -> redline_checks[]."
        )
        return

    check = redline_checks[0]
    if "anchor" not in check or "source_text_hash" not in check:
        failures.append(f"[2b] redline_checks[] entry missing anchor/source_text_hash: {check}")

    doc_result = eval_harness.score_document_level_case(case_raw, docx_bytes, playbook, standard)
    passed, reasons = eval_harness.check_redline_checks(case_raw, doc_result.hunks, standard)
    if not passed:
        failures.append(f"[2c] redline_checks verification against real patch output failed: {reasons}")

    # Negative control: a redline_checks entry with a GARBLED hash must be
    # caught, not silently accepted -- proving the assertion is real, not
    # a no-op that always passes.
    garbled_case = dict(case_raw)
    garbled_case["redline_checks"] = [{"anchor": check["anchor"], "source_text_hash": "sha256:garbled"}]
    garbled_passed, garbled_reasons = eval_harness.check_redline_checks(
        garbled_case, doc_result.hunks, standard
    )
    if garbled_passed:
        failures.append(
            "[2d] check_redline_checks() must FAIL a redline_checks entry whose "
            "source_text_hash does not match the diff's own hunk hash -- got passed=True."
        )
    if not garbled_reasons:
        failures.append("[2e] A failed redline_checks verification must report a reason.")


# ---------------------------------------------------------------------------
# 3. Smoke-tier model pass via FakeBedrockClient + recorded #81 fixtures.
# ---------------------------------------------------------------------------


def _load_model_response(name: str) -> str:
    return (MODEL_RESPONSES_DIR / name).read_text(encoding="utf-8")


def test_smoke_tier_model_pass_measures_decision_accuracy_and_missed_issues(
    failures: list[str],
) -> None:
    playbook = _load_playbook()
    policy = model_client.load_model_policy()
    model_id = model_client.primary_model_id(policy)

    # Two smoke cases driven by RECORDED #81 fixtures -- no live Bedrock.
    smoke_cases = [
        {
            "case_id": "smoke-reject",
            "response_fixture": "primary_request_change_valid.json",
            "expected_decision": "REQUEST_CHANGE",
            "expected_hard_topic_ids": {"limitation-of-liability"},
        },
        {
            "case_id": "smoke-accept",
            "response_fixture": "primary_accept_valid.json",
            "expected_decision": "ACCEPT",
            "expected_hard_topic_ids": set(),
        },
    ]

    results = []
    for sc in smoke_cases:
        client = model_client.FakeBedrockClient({model_id: [_load_model_response(sc["response_fixture"])]})
        result = eval_harness.score_smoke_tier_case(
            case_id=sc["case_id"],
            expected_decision=sc["expected_decision"],
            expected_hard_topic_ids=sc["expected_hard_topic_ids"],
            diff_hunks=[{"anchor": "sec-8", "kind": "modified_new", "text": "sample clause text"}],
            playbook=playbook,
            model_client_instance=client,
            model_id=model_id,
        )
        results.append(result)
        if not result.passed:
            failures.append(f"[3a] Smoke case {sc['case_id']!r} failed: {result.reasons}")
        # The FakeBedrockClient must actually have been invoked -- proving
        # this is a real (if fake) model call, not a bypassed no-op.
        if not client.calls:
            failures.append(f"[3b] Smoke case {sc['case_id']!r}: FakeBedrockClient.invoke() was never called.")

    decision_accuracy = sum(1 for r in results if r.actual_decision is not None) / len(results)
    missed_issue_rate = sum(1 for r in results if not r.passed) / len(results)
    if decision_accuracy != 1.0:
        failures.append(f"[3c] Expected smoke-tier decision accuracy 1.0, computed {decision_accuracy}.")
    if missed_issue_rate != 0.0:
        failures.append(f"[3d] Expected smoke-tier missed-issue rate 0.0, computed {missed_issue_rate}.")

    # Negative control: a response fixture that DISAGREES with the known
    # answer must be caught -- proving the comparison is real.
    wrong_client = model_client.FakeBedrockClient(
        {model_id: [_load_model_response("primary_accept_valid.json")]}
    )
    wrong_result = eval_harness.score_smoke_tier_case(
        case_id="smoke-reject-wrong-answer",
        expected_decision="REQUEST_CHANGE",
        expected_hard_topic_ids={"limitation-of-liability"},
        diff_hunks=[{"anchor": "sec-8", "kind": "modified_new", "text": "sample clause text"}],
        playbook=playbook,
        model_client_instance=wrong_client,
        model_id=model_id,
    )
    if wrong_result.passed:
        failures.append(
            "[3e] score_smoke_tier_case() must FAIL when the model's recorded response "
            "disagrees with the known answer -- got passed=True."
        )


# ---------------------------------------------------------------------------
# 4. Generator tautology broken: at least one hand-authored near-miss/
#    injection fixture the mechanical generator cannot produce.
# ---------------------------------------------------------------------------


def test_generator_tautology_is_broken_by_a_hand_authored_fixture(failures: list[str]) -> None:
    hand_authored = []
    for path in sorted(GOLD_FIXTURES_DIR.glob("*.json")):
        with open(path, "r", encoding="utf-8") as fh:
            raw = json.load(fh)
        if "generated_by" in raw:
            continue  # mechanically generated by scripts/generate_gold_fixtures.py
        if raw.get("case_type") in ("near_miss", "injection") and raw.get("synthetic") is True:
            hand_authored.append(raw["case_id"])

    if not hand_authored:
        failures.append(
            "[4a] No hand-authored near_miss/injection gold fixture found -- the mechanical "
            "generator (scripts/generate_gold_fixtures.py) builds its planted hunk FROM the "
            "rule's own first trigger term (a tautology); at least one fixture must carry "
            "independent signal the generator cannot produce."
        )

    # Sanity: the generator-produced fixtures (case_type absent, generated_by
    # present) must still exist alongside the hand-authored ones -- this
    # check proves the DISTINCTION is real, not that the generated set was
    # deleted.
    generated = [
        json.loads(p.read_text(encoding="utf-8"))["case_id"]
        for p in GOLD_FIXTURES_DIR.glob("*.json")
        if "generated_by" in json.loads(p.read_text(encoding="utf-8"))
    ]
    if not generated:
        failures.append("[4b] Expected the mechanically-generated fixture subset to still be present.")


# ---------------------------------------------------------------------------
# 5. docs/evaluation.md's "Recorded baseline (v1)" relabeled a projection.
# ---------------------------------------------------------------------------


def test_recorded_baseline_relabeled_as_projection(failures: list[str]) -> None:
    text = EVALUATION_DOC_PATH.read_text(encoding="utf-8")

    if "Recorded baseline (v1, established 2026-06-22)" in text:
        failures.append(
            "[5a] docs/evaluation.md still labels the v1 baseline table as a 'Recorded "
            "baseline' -- it must be relabeled a PROJECTION until a live stochastic gate "
            "run replaces it (the eval harness was not live when these numbers were "
            "written -- docs/evaluation.md's own text says so a few lines below the "
            "heading)."
        )

    if "projection" not in text.lower():
        failures.append(
            "[5b] docs/evaluation.md does not contain the word 'projection' anywhere -- "
            "the v1 baseline relabel is missing."
        )


# ---------------------------------------------------------------------------
# Runner
# ---------------------------------------------------------------------------

TESTS = [
    test_document_level_fixture_is_real_docx_not_a_snippet,
    test_document_level_case_scores_via_real_pipeline,
    test_document_level_case_fails_red_on_a_clean_document,
    test_redline_checks_populated_and_verified_against_patch_output,
    test_smoke_tier_model_pass_measures_decision_accuracy_and_missed_issues,
    test_generator_tautology_is_broken_by_a_hand_authored_fixture,
    test_recorded_baseline_relabeled_as_projection,
]


def main() -> int:
    failures: list[str] = []
    for test in TESTS:
        before = len(failures)
        try:
            test(failures)
        except Exception as exc:  # noqa: BLE001
            failures.append(f"[{test.__name__}] raised {type(exc).__name__}: {exc}")
        if len(failures) == before:
            print(f"PASS: {test.__name__}")
        else:
            for f in failures[before:]:
                print(f"FAIL: {f}")

    print()
    if failures:
        print(f"FAIL: {len(failures)} issue(s) found.")
        return 1
    print("PASS: all eval-harness quality (issue #204) assertions satisfied.")
    return 0


if __name__ == "__main__":
    sys.exit(main())
